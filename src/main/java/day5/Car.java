package day5;

public class Car {

    private String model;

    public void setModel(String userModel) {

        model = userModel;
    }

    public String getModel() {

        return model;
    }

    private String color;

    public void setColor(String userColor) {

        color = userColor;
    }

    public String getColor() {

        return color;
    }

    private int year;

    public void setYear(int userYear) {

        year = userYear;
    }

    public int getYear() {

        return year;
    }


}

